    private void jBtn_ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtn_ExcluirActionPerformed
        // TODO add your handling code here:
            int resp = JOptionPane.showConfirmDialog(null, "Confirma Exclusão ?","Atenção", JOptionPane.YES_NO_OPTION);
            if (resp == JOptionPane.YES_OPTION){
                Dj_Usuarios dj_usuarios = new Dj_Usuarios();
                int codigo = Integer.parseInt(jTxt_Codigo.getText());
                dj_usuarios.setDj_idUsuarios(codigo);
                dj_usuarios.setDj_nome(jTxt_Nome.getText() );
                dj_usuarios.setDj_apelido(jTxt_Apelido.getText() );
                dj_usuarios.setDj_cpf(jFmt_Cpf.getText() );
                dj_usuarios.setDj_dataNascimento(null);
                dj_usuarios.setDj_senha(jPwd_Senha.getText() );
                dj_usuarios.setDj_nivel(jCbo_Nivel.getSelectedIndex());
                if (jChb_Ativo.isSelected() == true){
                    dj_usuarios.setDj_ativo("S");
                } else {
                    dj_usuarios.setDj_ativo("N");
                }
                Dj_UsuariosDao usuariosDao = new Dj_UsuariosDao();
                usuariosDao.delete(dj_usuarios);
            }
            limpar();
    }//GEN-LAST:event_jBtn_ExcluirActionPerformed

