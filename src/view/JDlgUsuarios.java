    private void jBtn_PesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtn_PesquisarActionPerformed
        // TODO add your handling code here:
        String id = JOptionPane.showInputDialog(null, "Pesquisar o código");
        int codigo = Integer.parseInt(id);
        Dj_UsuariosDao usuariosDao = new Dj_UsuariosDao();
        Dj_Usuarios dj_usuarios = (Dj_Usuarios) usuariosDao.list(codigo);
        if (dj_usuarios == null){
            JOptionPane.showMessageDialog(null, "Códgio não existe");
        } else{
        jTxt_Codigo.setText(id);
        jTxt_Nome.setText(dj_usuarios.getDj_nome());
        jTxt_Apelido.setText(dj_usuarios.getDj_apelido());
        jFmt_Cpf.setText(dj_usuarios.getDj_cpf());
//        jFmt_DataNascimento.setText(dj_usuarios.getDj_dataNascimento());
        jPwd_Senha.setText(dj_usuarios.getDj_senha());
        jCbo_Nivel.setSelectedIndex(dj_usuarios.getDj_nivel());
        if (dj_usuarios.getDj_ativo().equals("S")) {
            jChb_Ativo.setSelected(true);
        }else{
            jChb_Ativo.setSelected(false);        
        }
    }
    }//GEN-LAST:event_jBtn_PesquisarActionPerformed

