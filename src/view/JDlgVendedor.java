    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        // TODO add your handling code here:
            int resp = JOptionPane.showConfirmDialog(null, "Confirma Exclusão ?","Atenção", JOptionPane.YES_NO_OPTION);
            if (resp == JOptionPane.YES_OPTION){
                Dj_Vendedor dj_Vendedor=  new Dj_Vendedor();
                int codigo = Integer.parseInt(jTxt_Codigo.getText());
                dj_Vendedor.setDj_idVendedor(codigo);
                dj_Vendedor.setDj_nome(jTxt_Nome.getText() );
                dj_Vendedor.setDj_cpf(jFmt_CPF.getText() );
                dj_Vendedor.setDj_telefone(jFmt_Telefone.getText() );
                dj_Vendedor.setDj_email(jTxt_Email.getText() );
                dj_Vendedor.setDj_endereco(jTxt_Endereco.getText() );
                dj_Vendedor.setDj_cep(jFmt_CEP.getText());

                Dj_VendedorDao vendedorDao = new Dj_VendedorDao();
                vendedorDao.delete(dj_Vendedor);
            }
            limpar();
    }//GEN-LAST:event_jBtnExcluirActionPerformed

    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
        // TODO add your handling code here:
        Dj_Vendedor dj_Vendedor = new Dj_Vendedor();
        int codigo = Integer.parseInt(jTxt_Codigo.getText());
        dj_Vendedor.setDj_idVendedor(codigo);
        dj_Vendedor.setDj_nome(jTxt_Nome.getText() );
        dj_Vendedor.setDj_cpf(jFmt_CPF.getText() );
        dj_Vendedor.setDj_telefone(jFmt_Telefone.getText() );
        dj_Vendedor.setDj_email(jTxt_Email.getText() );
        dj_Vendedor.setDj_endereco(jTxt_Endereco.getText());
        dj_Vendedor.setDj_cep(jFmt_CEP.getText());
        Dj_VendedorDao vendedorDao = new Dj_VendedorDao();
        vendedorDao.insert(dj_Vendedor);
        limpar();
        Habilitar(false);
    }//GEN-LAST:event_jBtnConfirmarActionPerformed

