    private void jBtnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnPesquisarActionPerformed
        // TODO add your handling code here:
        String id = JOptionPane.showInputDialog(null, "Pesquisar o código");
        int codigo = Integer.parseInt(id);
        Dj_VendedorDao vendedorDao = new Dj_VendedorDao();
        Dj_Vendedor dj_Vendedor = (Dj_Vendedor) vendedorDao.list(codigo);
        if (dj_Vendedor == null){
            JOptionPane.showMessageDialog(null, "Códgio não existe");
        } else{
        jTxt_Codigo.setText(id);
        jTxt_Nome.setText(dj_Vendedor.getDj_nome());
        jFmt_CPF.setText(dj_Vendedor.getDj_cpf());
        jFmt_Telefone.setText(dj_Vendedor.getDj_telefone());
        jTxt_Email.setText(dj_Vendedor.getDj_email());
        jTxt_Endereco.setText(dj_Vendedor.getDj_endereco());
        jFmt_CEP.setText(dj_Vendedor.getDj_cep());
    }
    }//GEN-LAST:event_jBtnPesquisarActionPerformed

